package dto.chat;

public class UserDTO {
	
	private String userID;
	private String userPassword;
	private String userName;
	private int userAge ;
	private String userGender;
	private String userEmail;
	private String userProfile;
	
	public final String getUserID() {
		return userID;
	}
	public final void setUserID(String userID) {
		this.userID = userID;
	}
	public final String getUserPassword() {
		return userPassword;
	}
	public final void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}
	public final String getUserName() {
		return userName;
	}
	public final void setUserName(String userName) {
		this.userName = userName;
	}
	public final int getUserAge() {
		return userAge;
	}
	public final void setUserAge(int userAge) {
		this.userAge = userAge;
	}
	public final String getUserGender() {
		return userGender;
	}
	public final void setUserGender(String userGender) {
		this.userGender = userGender;
	}
	public final String getUserEmail() {
		return userEmail;
	}
	public final void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
	public final String getUserProfile() {
		return userProfile;
	}
	public final void setUserProfile(String userProfile) {
		this.userProfile = userProfile;
	}
	
	
	
}
